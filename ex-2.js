// add function
const add = function (a, b) {
  return a + b;
};
console.log(add(10, 5));

// subtract function
const subtract = function (a, b) {
  return a - b;
};
console.log(subtract(10, 5));

// multiply function
const multiply = function (a, b) {
  return a * b;
};
console.log(multiply(10, 5));

// divide function
const divide = function (a, b) {
  return a / b;
};
console.log(divide(10, 5));
